package Lesson_12.IceCream;

public class Sugar {

    double allSugar;
    double sumSugar;
    public Sugar(double allSugar, double sumSugar){
        this.allSugar=allSugar;
        this.sumSugar=sumSugar;
    }

    public double getAllSugar() {
        return allSugar;
    }

    public void setAllSugar(double allSugar) {
        this.allSugar = allSugar;
    }
}
