package Lesson_12.IceCream;

public class Milk {
    double allMilk;
    double sumMilk;

    public Milk(double allMilk, double sumMilk) {
        this.allMilk = allMilk;
        this.sumMilk = sumMilk;
    }

    public double getAllMilk() {
        return allMilk;
    }

    public void setAllMilk(double allMilk) {
        this.allMilk = allMilk;
    }
}
