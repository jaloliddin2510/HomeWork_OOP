package Lesson_14.HomeWork_2;

public class Book {
    private String nameBook;
    private double sumBook;

    public Book(String nameBook, double sumBook) {
        this.nameBook = nameBook;
        this.sumBook = sumBook;
    }

    public String getNameBook() {
        return nameBook;
    }

    public double getSumBook() {
        return sumBook;
    }
}
