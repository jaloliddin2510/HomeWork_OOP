package Lesson_14.HomeWork_1;

public class Car {

    private int slotBattery;
    private double powerEngine;

    public Car(int slotBattery, double powerEngine) {
        this.slotBattery = slotBattery;
        this.powerEngine = powerEngine;
    }

    public int getSlotBattery() {
        return slotBattery;
    }

    public double getPowerEngine() {
        return powerEngine;
    }

}
